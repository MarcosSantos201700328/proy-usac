    private void startprocs() {  
        w = new Thread((Runnable)waitq);
        currThread = new Thread(this);
        w.start();     
        currThread.start();
    }

    private void stopprocs() {
        w.yield();
        currThread.yield();
    }

    public void run() {
        Pcb tmp;
        while(waitq.getProcesos().size() > 0 && exec){
            try{
                //w.wait();
                tmp = waitq.Pop();
                jLabelRun.setText(tmp.getId() + " " + tmp.getName() + " " + tmp.getQuantum());
                //w.notify();
                //w.sleep((int)tmp.getQuantum() * 1000);
                currThread.sleep((int)tmp.getQuantum() * 1000);
                waitq.Push(tmp);
                //w.notify();
            }
            catch(InterruptedException e){
                
            }
//            finally{
//                this.repaint();
//           }
        }
    }
    
