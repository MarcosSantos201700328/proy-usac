    private void jButtonExecActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExecActionPerformed
        if(exec){
            jButtonExec.setText("Ejecutar");
            jButtonAdd.setEnabled(true);
            jButtonGetSystem.setEnabled(true);            
            this.jRadioButtonAuto.setEnabled(true);
            this.jRadioButtonMan.setEnabled(true);
            exec = false;
            stopprocs();
        }
        else{
            jButtonExec.setText("Detener");
            jButtonAdd.setEnabled(false);            
            jButtonGetSystem.setEnabled(false);
            this.jRadioButtonAuto.setEnabled(false);
            this.jRadioButtonMan.setEnabled(false);
            exec = true;
            startprocs();
        }
            
        
    }//GEN-LAST:event_jButtonExecActionPerformed

